from math import *

print(10 % 3) #remainder

print(type(2 + 5j)) #Complex number

#Convert number to string
num1 = 50
print(str(num1) + ' is my favourite number')

#Absolute value
my_num = -10
print(abs(my_num))

#Power of number
print(pow(2, 2))

#Get max number
print(max(2 , 3, 9, 78))

#Get min number
print(min(2 , 3, 9, 78))

#Round a number
print(round(3.58))

#Ceilling a number
print(ceil(3.32))

#Floor number
print(floor(3.8))

#Square a number
print(sqrt(36))